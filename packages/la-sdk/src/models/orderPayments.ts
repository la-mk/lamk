import merge from 'lodash/merge';
import { Application, Params } from '@feathersjs/feathers';
import { getCrudMethods } from '../setup';
import { OmitServerProperties } from '../utils';
import { validate, validateSingle } from '../utils/validation';
import v8n from 'v8n';

export enum TransactionStatus {
  APPROVED = 'approved',
  DECLINED = 'declined',
  ERROR = 'error',
}

export const paymentTransactionSchema = {
  transactionStatus: v8n().oneOf(Object.values(TransactionStatus)),
  transactionMessage: v8n().optional(
    v8n()
      .string()
      .minLength(2)
      .maxLength(511)
  ),
  processorTransactionId: v8n().optional(
    v8n()
      .string()
      .minLength(2)
      .maxLength(63)
  ),
  userIp: v8n().optional(
    v8n()
      .string()
      .minLength(2)
      .maxLength(31)
  ),
};

export const schema = {
  // ID is optional as it is autogenerated by server on creation.
  _id: v8n().optional(
    v8n()
      .string()
      .minLength(2)
      .maxLength(63),
    true
  ),
  forOrder: v8n()
    .string()
    .minLength(2)
    .maxLength(63),
  transactions: v8n().minLength(1).every.schema(paymentTransactionSchema),
  // Calculated field based on all transactions.
  isSuccessful: v8n().boolean(),
  // createdAt is optional as it is added by server on creation.
  createdAt: v8n().optional(
    v8n()
      .string()
      .minLength(2)
      .maxLength(63),
    true
  ),
  modifiedAt: v8n().optional(
    v8n()
      .string()
      .minLength(2)
      .maxLength(63),
    true
  ),
};

export interface PaymentTransaction {
  transactionStatus: string;
  transactionMessage?: string;
  processorTransactionId?: string;
  userIp?: string;
}

export interface OrderPayments {
  _id: string;
  forOrder: string;
  transactions: PaymentTransaction[];
  isSuccessful: boolean;
  createdAt?: string;
  modifiedAt?: string;
}

export const getOrderPaymentsSdk = (client: Application) => {
  const crudMethods = getCrudMethods<
    OmitServerProperties<OrderPayments>,
    OrderPayments
  >(client, 'orderPayments');

  return {
    ...crudMethods,
    findForOrder: (orderId: string, params?: Params) => {
      const options = {};
      merge(options, params, { query: { forOrder: orderId } });
      return crudMethods.find(options);
    },

    validate: (data: OrderPayments, ignoreRequired = false) => {
      return validate(schema, data, ignoreRequired);
    },
    validateSingle: (val: any, selector: string) => {
      return validateSingle(schema, val, selector);
    },

    TransactionStatus,
  };
};
